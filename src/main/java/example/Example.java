package example;

public class Example {

    /**
     * Counts the sum of two arguments
     * @cpu O(1), n - this.place
     * @ram O(1), n - this.place
     * @param a argument
     * @param b argument
     * @return sum of two arguments
     */
    public static int sum(int a, int b) {
        return a + b;
    }
}
